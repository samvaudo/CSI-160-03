# Working with lists

x = []  # empty list, not an empty variable
# print(x1)


x = ['a', 'b', 3, True]  # What is the data type? Mixed/Different for each element
# print(x)

for i in x:  # The interpreter takes the first element in the list, passes it to the variable 'i'
    # The value if 'i' is printed and then the next element is passed to 'i' is printed
    print(i)

# 0 is the starting value in a list

print(x[3])  # Prints the element in the index position 3
print(x[-1])  # -1 is the end of the list, -2 is the second to end, and so on

